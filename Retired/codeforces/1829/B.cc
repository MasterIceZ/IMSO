using ll = long long;

inline void solution(){
	int n, cnt = 0;
	cin >> n;
	vector<int> v(n + 1);
	v[0] = 1;
	int maxx = 0;
	for(int i=1; i<=n; ++i){
		cin >> v[i];
		if(v[i] == 0){
			cnt++;
			maxx = max(maxx, cnt);
		}
		else{
			cnt = 0;
		}
	}
	cout << maxx;
	return ;
}

signed main(){
	cin.tie(nullptr)->ios::sync_with_stdio(false);	
	int q = 1;
	cin >> q;
	while(q--){
		solution();
		cout << "\n";
	}
	return 0;
}
