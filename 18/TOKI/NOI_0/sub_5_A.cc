using ll = long long;

string s, answer;

inline void solution(){
	int n;
	cin >> n >> s;
	answer = s;
	sort(s.begin(), s.end());
	bool ok;
	do{
		ok = true;
		for(int i=1; i<n-1; ++i){
			ok &= (s[i] != s[i - 1] && s[i] != s[i + 1]);
		}
		if(ok){
			answer = min(answer, s);
		}
	}while(next_permutation(s.begin(), s.end()));
	cout << answer;
	return ;
}

signed main(){
	cin.tie(nullptr)->ios::sync_with_stdio(false);	
	int q = 1;
//	cin >> q;
	while(q--){
		solution();
		cout << "\n";
	}
	return 0;
}
